var musician = 3;

if (musician == 0) {
  console.log("nota group");
} else if (musician == 1) {
  console.log("Solo");
} else if (musician == 2) {
  console.log("Duet");
} else if (musician == 3) {
  console.log("Trio");
} else if (musician >= 4) {
  console.log("Large group");
} else if (musician == -1) {
  console.log("not a group");
}
